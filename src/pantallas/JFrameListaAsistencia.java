    public void agregarAsistencia(Asistencia asistenciaNueva) {
        listaAsistencias.add(asistenciaNueva);
        modeloAsistencias.removeAllElements();

        for (Asistencia asistenciaLista : listaAsistencias) {
            modeloAsistencias.addElement(asistenciaLista.mostrarDatos());
        }
    }

    private void cargarAsistencias(String buscador) {

        modeloAsistencias.removeAllElements();
        for (Asistencia asistencia : listaAsistencias) {

            if (asistencia.getAlumno().getNombre().contains(buscador)) {
                modeloAsistencias.addElement(asistencia.mostrarDatos());
            }
        }
    }

